import { useState } from "react";

export default function Home() {
  const [input, setInput] = useState("");
  const [data, setData] = useState([]);
  const [header, setHeader] = useState([]);
  const [textData, setTextData] = useState([]);
  const [homeData, setHomeData] = useState([]);
  const [activeSheet, setActiveSheet] = useState("GP");
  const [error, setError] = useState("");
  const [selectedCells, setSelectedCells] = useState(new Set());

  // üîç SEARCH API
  const handleSearch = async () => {
    setError("");
    setData([]);
    try {
      const res = await fetch(`/api/check?keyword=${encodeURIComponent(input)}`);
      const json = await res.json();
      if (json.results) {
        setHeader(json.header);
        setData(json.results);
        setTextData(json.textData);
        setHomeData(json.homeData);
      } else {
        setError(json.message || "Kh√¥ng t√¨m th·∫•y");
      }
    } catch (err) {
      setError("L·ªói server");
    }
  };

  // üìä L·∫•y gi√° t·ª´ sheet kh√°c
  const getPriceFromOtherSheet = (site, sheet) => {
    const source = sheet === "TEXT" ? textData : homeData;
    const match = source.find((row) => row[4] === site);
    if (match) {
      return { giaBan: match[9] || "", giaMua: match[10] || "" };
    }
    return null;
  };

  // üìã Double click copy 1 √¥
  const handleDoubleClickCopy = (text) => {
    navigator.clipboard.writeText(text);
    alert(`‚úÖ ƒê√£ copy: ${text}`);
  };

  // ‚úÖ Click ch·ªçn nhi·ªÅu √¥
  const handleCellClick = (rowIndex, colIndex) => {
    const cellId = `${rowIndex}-${colIndex}`;
    const newSelection = new Set(selectedCells);

    if (newSelection.has(cellId)) {
      newSelection.delete(cellId);
    } else {
      newSelection.add(cellId);
    }
    setSelectedCells(newSelection);
  };

  // üìã Copy t·∫•t c·∫£ √¥ ƒë√£ ch·ªçn (d·∫°ng 1 h√†ng ngang)
  const handleCopySelected = () => {
    const rows = [];
    let currentRow = -1;
    let temp = [];

    Array.from(selectedCells)
      .map((id) => id.split("-").map(Number))
      .sort((a, b) => (a[0] - b[0]) || (a[1] - b[1]))
      .forEach(([row, col]) => {
        if (row !== currentRow) {
          if (temp.length > 0) rows.push(temp.join("\t"));
          temp = [];
          currentRow = row;
        }
        temp.push(data[row][col]);
      });

    if (temp.length > 0) rows.push(temp.join("\t"));

    navigator.clipboard.writeText(rows.join("\n"));
    alert(`‚úÖ ƒê√£ copy ${selectedCells.size} √¥!`);
  };

  return (
    <div style={{ padding: "20px", fontFamily: "Arial", backgroundColor: "#fafafa", minHeight: "100vh" }}>
      <h2>Tool Check Site (Demo)</h2>

      {/* üì• √î nh·∫≠p */}
      <textarea
        rows={3}
        style={{ width: "450px", padding: "8px", borderRadius: "5px", border: "1px solid #ccc" }}
        placeholder="Nh·∫≠p site ho·∫∑c m√£ (m·ªói d√≤ng 1 gi√° tr·ªã)"
        value={input}
        onChange={(e) => setInput(e.target.value)}
      />

      <br />
      {/* üîç N√∫t search */}
      <button
        onClick={handleSearch}
        style={{
          marginTop: "10px",
          padding: "10px 22px",
          backgroundColor: "#2E8B57",
          color: "#fff",
          border: "none",
          borderRadius: "5px",
          fontWeight: "bold",
          cursor: "pointer",
        }}
      >
        üîç T√¨m ki·∫øm
      </button>

      {error && <p style={{ color: "red", marginTop: "10px" }}>{error}</p>}

      {/* ‚úÖ 3 N√öT CH·ªåN SHEET */}
      {data.length > 0 && (
        <div style={{ marginTop: "20px" }}>
          <button
            onClick={() => setActiveSheet("GP")}
            style={{
              marginRight: "10px",
              backgroundColor: activeSheet === "GP" ? "#28a745" : "#ccc",
              color: "#fff",
              padding: "8px 16px",
              border: "none",
              borderRadius: "5px",
              cursor: "pointer"
            }}
          >
            GP
          </button>
          <button
            onClick={() => setActiveSheet("TEXT")}
            style={{
              marginRight: "10px",
              backgroundColor: activeSheet === "TEXT" ? "#28a745" : "#ccc",
              color: "#fff",
              padding: "8px 16px",
              border: "none",
              borderRadius: "5px",
              cursor: "pointer"
            }}
          >
            TEXT
          </button>
          <button
            onClick={() => setActiveSheet("HOME")}
            style={{
              backgroundColor: activeSheet === "HOME" ? "#28a745" : "#ccc",
              color: "#fff",
              padding: "8px 16px",
              border: "none",
              borderRadius: "5px",
              cursor: "pointer"
            }}
          >
            HOME
          </button>
        </div>
      )}

      {/* ‚úÖ N√∫t Copy */}
      {selectedCells.size > 0 && (
        <div style={{ marginTop: "10px" }}>
          <button
            onClick={handleCopySelected}
            style={{
              backgroundColor: "#007bff",
              color: "#fff",
              padding: "8px 16px",
              borderRadius: "5px",
              border: "none",
              cursor: "pointer",
            }}
          >
            üìã Copy {selectedCells.size} √¥
          </button>
        </div>
      )}

      {/* üìä B·∫£ng d·ªØ li·ªáu */}
      {data.length > 0 && (
        <table
          style={{
            marginTop: "20px",
            borderCollapse: "collapse",
            width: "100%",
            backgroundColor: "#fff",
            boxShadow: "0 2px 8px rgba(0,0,0,0.1)",
          }}
        >
          <thead>
            <tr>
              {header.map((h, i) => (
                <th
                  key={i}
                  style={{
                    padding: "10px",
                    backgroundColor: "#f0f0f0",
                    borderBottom: "2px solid #ddd",
                    fontWeight: "bold",
                    textAlign: "center",
                  }}
                >
                  {h}
                </th>
              ))}
            </tr>
          </thead>
          <tbody>
            {data.map((row, rowIndex) => {
              const site = row[4];
              let rowCopy = [...row];

              // üîÑ Update gi√° khi ch·ªçn TEXT ho·∫∑c HOME
              if (activeSheet !== "GP") {
                const newPrice = getPriceFromOtherSheet(site, activeSheet);
                if (newPrice) {
                  rowCopy[9] = newPrice.giaBan;
                  rowCopy[10] = newPrice.giaMua;
                }
              }

              return (
                <tr key={rowIndex} style={{ borderBottom: "1px solid #eee" }}>
                  {rowCopy.map((cell, colIndex) => {
                    const cellId = `${rowIndex}-${colIndex}`;
                    const isSelected = selectedCells.has(cellId);

                    return (
                      <td
                        key={colIndex}
                        onClick={() => handleCellClick(rowIndex, colIndex)}
                        onDoubleClick={() => handleDoubleClickCopy(cell)}
                        style={{
                          padding: "8px",
                          textAlign: "center",
                          cursor: "pointer",
                          backgroundColor: isSelected ? "#cce5ff" : "transparent",
                          border: "1px solid #ddd",
                        }}
                      >
                        {cell}
                      </td>
                    );
                  })}
                </tr>
              );
            })}
          </tbody>
        </table>
      )}
    </div>
  );
}
